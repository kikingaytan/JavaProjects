package nodos.reportes.dto;


/**
 * The Class RHDTO is the test class necessary for the Prove of concept.
 */
public class RHDTO {

	/** The costo persona. */
	private String costoPersona;
	
	/** The humanoid. */
	private String humanoid;
	
	/** The numero recursos humanos. */
	private String numeroRecursosHumanos;
	
	/**
	 * Gets the costo persona.
	 *
	 * @return the costo persona
	 */
	public String getCostoPersona() {
		return costoPersona;
	}
	
	/**
	 * Gets the humanoid.
	 *
	 * @return the humanoid
	 */
	public String getHumanoid() {
		return humanoid;
	}
	
	/**
	 * Gets the numero recursos humanos.
	 *
	 * @return the numero recursos humanos
	 */
	public String getNumeroRecursosHumanos() {
		return numeroRecursosHumanos;
	}
	
	/**
	 * Sets the costo persona.
	 *
	 * @param costoPersona the new costo persona
	 */
	public void setCostoPersona(String costoPersona) {
		this.costoPersona = costoPersona;
	}
	
	/**
	 * Sets the humanoid.
	 *
	 * @param humanoid the new humanoid
	 */
	public void setHumanoid(String humanoid) {
		this.humanoid = humanoid;
	}
	
	/**
	 * Sets the numero recursos humanos.
	 *
	 * @param numeroRecursosHumanos the new numero recursos humanos
	 */
	public void setNumeroRecursosHumanos(String numeroRecursosHumanos) {
		this.numeroRecursosHumanos = numeroRecursosHumanos;
	}
}
